def startHelp():  # help display
    global startCommands
    commands = list(startCommands.keys())
    commandDescriptions = {
        "cglog": "shows the changelog",
        "check": "toggles recipe-check mode",
        "help": "gives the list of commands",
        "start": "starts the main program",
    }
    helpText = """List of commands:
"""
    for command in commands:
        helpText += f"""
{command}: """
        if command in commandDescriptions.keys():
            helpText += commandDescriptions[command]
        else:
            helpText += """Unknown command."""
    print(helpText)


